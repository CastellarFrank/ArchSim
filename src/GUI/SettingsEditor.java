    public String indexToTheme(int index) {
        if (index == 0)
            return "themes/default.xml";
        else if (index == 1)
            return "themes/dark.xml";
        else if (index == 2)
            return "themes/vs.xml";
        else if (index == 3)
            return "themes/eclipse.xml";
        else if (index == 4)
            return "themes/idea.xml";
        return "";
    }

